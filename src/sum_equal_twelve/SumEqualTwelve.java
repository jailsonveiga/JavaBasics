package sum_equal_twelve;

public class SumEqualTwelve {
    public static void main (String[] args) {
        //Initialize integer datatype with variable name a and assign value of 3 and store into variable a
        int a = 3;

        //Initialize integer datatype with variable name b and assign value of 5 and store into variable b
        int b = 5;

        // add sum of 4 + value of a "3" plus value of "5" and the total sum store in a variable sum
        int sum = 4 + a + b;

        //Display "Sum = 12" on the screen
        System.out.print("Sum = " + sum);
    }
}
