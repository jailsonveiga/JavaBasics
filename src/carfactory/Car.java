package carfactory;

public class Car {
    public String make;
    public String model;
    public int mileage;
    public float gasTankPercentage;

    public Car(String make, String model, int mileage, float gasTankPercentage) {
        this.make = make;
        this.model = model;
        this.mileage = mileage;
        this.gasTankPercentage = gasTankPercentage;
    }

}


