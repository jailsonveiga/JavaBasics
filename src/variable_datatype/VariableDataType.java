package variable_datatype;

public class VariableDataType {
    public static void main (String[] args) {
        //Declare a Variable and Its DataType

        //Declaring an Integer DataType With Variable Name a
        int a;

        //Declaring an String DataType With Variable Name s
        String s;

        //Assign Value 15 into Variable a
        a = 15;

        //Assign Value "Hello World" into Variable s
        s = "Hello World";

        //Display 15 On The Screen
        System.out.println (a);

        //Display "Hello World" On The Screen
        System.out.println (s);
    }
}
