package for_loop;

public class ForLoopTenToOne {
    public static void main(String[] args) {
        //Initialize data type i and assign initial value to 10 and store the value into i
        //Check if i value is less than equal 5
        //subtract i value by 1 after each iteration
        for (int i = 10; i >= 5 ; i--) {
            //Display message on the screen
            System.out.println("Print number " + i);
        }
    }
}
