package while_loop;

public class OneHundredTimes {
    public static void main(String[] args) {
        //Initialize data type i and assign value 1 and store the value into variable i
        int i = 1;

        //As long value i is less or equal 100 continue the iteration
        while(i <= 100) {
            //Display message on the screen
            System.out.println("Print number: " + i);
            //increase i by 1 after each iteration
            i++;
        }
    }
}
